            <h1 class="display-4">Blood bank</h1>
            <p class="lead">The idea of ​​bringing another person back from the brink of death with the blood of a human being is not very old. While such an idea is unbelievable and bizarre before the modern era, this incredible idea is true for the benefit of science in the modern era. In the blood of one person of the whole group, another person is getting his life back. This has resulted in blood banks and voluntary blood donation programs. Our blood bank center, which is always engaged in the service of humanity, has created a blood bank to meet the blood needs of dying patients.

            </p>
            
            
              <h4>Benefits</h4>
              <p>1. All types of blood groups are available here.</p>
              <p>2. All types of processed blood are available here.</p>
              <p>3. Blood is collected from blood donors, purified and given to blood recipients.</p>
              <p>4. Here, as a source of blood, blood is collected from those who donate blood voluntarily and given to the recipients. Our Blood Donation Center tries to help as much as possible by sending their team to organize voluntary blood donation programs.</p>
              <p>5. Blood donors are usually provided with liquid food after blood donation.</p>
              
              <p> 6. A 3 inch syringe is usually used to collect blood. Once the blood is taken, the syringe is no longer used.</p>
              <p> 7. After donating blood, blood donors are given a type of ID card so that they can understand that this person has donated blood to their blood donation center.
                 </p>
                 <h4>Technical skills</h4>
                 <p>Our Blood Donation Center uses Aliza and (Rapid) technology for blood testing, scanning, matching, etc.</p>
                 <h4>Blood donation process</h4>
                 <p>In order to donate blood, they have to fill in a form. If there is no problem in the blood, then they take blood from the blood donors. In order to receive blood, you have to submit a written blood requisition with the seal and signature of the medical officer, then the blood will be available.</p>
             <h4>Blood preservation process</h4>
             <p>Blood Storage Process and Technology The blood is stored in a bag called CDDA 1, in which the blood stays good for 35 days and in this bag the blood is stored for 30 sec. Stays in temperature.
            </p>
            <h4>Blood collection process</h4>
            <p>Contact our Blood Donation Center to collect blood. Blood recipients do not have to wait long here.
              If you want to collect blood, you can easily get blood for 800 rupees by filling the receipt through cash.
              When blood is not available on demand, they communicate with their donors on their mobile phones and seek refuge at the Quantum Blood Center.
               </p>
               <h4>Special benefits</h4>
               <p>Blood is usually supplied to the general public and the poor helpless patients. Blood is given at Rs. 250 for patients who are in free beds in government hospitals and Rs. 450 for patients who are in clinics.
                There are separate special discounts for thalassemia patients. Patients with thalassemia are offered a 40% to 50% discount if they apply to the authorities.
                 </p>
                 <h4>Open-close schedule</h4>
                 <p>Our Blood Donation Center is open 24 hours a day, 7 days a week.</p>
