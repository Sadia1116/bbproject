            For many patients, blood donors are their lifeline. One unit of blood can save three lives!
            <br>World Blood Donor Day 2020 has arrived with the campaign theme “Safe blood saves lives” and slogan “Give blood and make the world a healthier place”. This idea focuses on how an individual blood donor can contribute to improve the health of other persons in the community. The world needs blood donors to deliver safe blood to sick people who are struggling to survive injuries, surgeries, or diseases. Though Blood donation is a noble cause in every civilised society on earth, many healthy adults fear to donate blood. Read this article to know the basic concepts and benefits of blood donation.</br>
            
