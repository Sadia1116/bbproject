        <div class="container">
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Lowered Risk of Heart Attack & Liver Diseases</h3>
          <p>Iron overload is defined as excess storage of iron in heart, liver, endocrine glands and different organs throughout the body. In 2013, a research revealed that the more iron stock in the body the higher risk of heart diseases and liver ailments. You can prevent iron-overloading in your body by donating blood regularly. Thus, the risk of cardiovascular and liver diseases will be minimised naturally.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Reduced Risk of Hemochromatosis</h3>
          <p>Hemochromatosis is a kind of disease which is caused due to the excess iron absorption by the human body. Reducing the risk iron-overload in the body, Blood donation can prevent the risk of occurring Hemochromatosis disease.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Lowered Risk of Cancer</h3>
          <p>Cancer is the world’s second leading cause of human deaths. Blood donation may assist in lowering the risk of cancer. If you can keep the iron level of your body under balance, then the risk of liver cancer, lungs cancer, and intestine cancer may get lowered.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Production of New Blood Cells</h3>
          <p>Within several weeks of donating blood, your body is expected to restore the loss by producing more blood cells. Productions of the new blood cells can strengthen your immunity and improve health.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Weight Maintenance</h3>
          <p>Are you struggling to shed some extra pounds from your body? Health-experts recommend donating blood for the obese adults. Donating blood on a regular basis can burn fat up to 650 calories and assist in your weight-loss journey.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Lowered Risk of Premature Ageing</h3>
          <p>Getting the opportunity to help people by donating blood will bestow you heavenly mental peace and satisfaction. It would help release mental stress, which is one of the major factors causing premature ageing. After giving blood, the body produces new blood cells which keep the skin youthful, and wrinkle-free.</p>
          <img src="https://www.wellthy.care/wp-content/uploads/2017/12/Untitled-design-2-696x392.jpg" class="img-fluid" alt="Responsive image">
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft  py-3" data-wow-delay="0.3s">Faster Healing Process</h3>
          <p>When you donate blood, your body will work on adjusting the loss of red blood cells. These adjustments lead to the production of new blood cells, which accelerate the process of healing wounds.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Lowered cholesterol level</h3>
          <p>Some research works found that donating blood regularly is a great way to lower the level of cholesterol, lipid, and triglyceride. Furthermore, blood cells are built with iron, excess of which can enhance the chances of developing blockage in blood vessels. By donating blood regularly, you can prevent iron overload and maintain your cholesterol.</p>
          <h3 class="h1-responsive font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Free medical checkup</h3>
          <p>Prior to donating blood, every blood donor is supposed to undertake a routine medical checkup for several parameters including body temperature, blood pressure, pulse, hemoglobin level, etc. In the process of blood storage, the collected blood is tested for about 13 infectious disease like HIV, hepatitis B and C, Syphilis, etc. Thus, donating blood can get you a free medical checkup.</p>
          <h3 class="h1-responsive display-5 font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Who Can Donate Blood?</h3>
          <p>To become a blood donor, you need to fulfill some basic eligibility requirements:
            <br>- You must be aged between 18 and 65.</br>
            <br>- Your weight must be at least 50 kg.</br>
            <br>- You must be in good health during donating blood.</br>
            <br>- For regular blood donation, you must wait at least 8 to 16 weeks between donations.</br>
            
          </p>
          <h3 class="h1-responsive display-5 font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Some additional guidelines you need to follow before donating blood</h3>
          <p>- You cannot donate blood if you are suffering from cold, flu, sore throat, stomach ache, or any other infection.
           <br>- If you have undergone a minor dental treatment you must wait for at least 24 hours interval. For major dental treatment, wait for at least a month before donating blood.</br>
           <br>- For donating blood you must meet the minimum hemoglobin level – 12.0 g/dl for females and 13.0 g/dl for males.</br>
           <br>- You must not give blood if you have ever tested positive for HIV (AIDS virus)</br>
           <br>- You must not donate blood if you have ever injected any recreational drugs.</br>
           <br>- You should not donate blood if you have recently travelled to any malaria-prone areas.</br>
           <br>- You are advised not to donate blood during the periods of pregnancy, post-natal, and breast-feeding.</br>
           
          </p>
          <h3 class="h1-responsive display-5 font-weight-bold wow fadeInLeft" data-wow-delay="0.3s">Why the World Needs More Voluntary Blood Donors?</h3>
          <p>Every year road accidents kill around 1.3 million people, leaving about 20 to 50 million people wounded or disabled around the world. About 90 percent of these casualties occur in the less developed countries. Uncontrolled bleeding is responsible for more than 4.68 lakh deaths worldwide per year.
            <br>Furthermore, about 3 lakh of infants are born per year worldwide with Thalassemia and sick-cell disease. These children need blood transfusion regularly. Besides these, blood transfusions are often prescribed for the treatment of pregnancy complications, childbirth, anemia, trauma, congenital blood disorders, etc.
          <br>Still today medical science is unable to manufacture blood. The only way to get blood is collecting it from the people who are willing to donate. The donated blood is stored at the blood bank to be used in future when needed.</br></br>
          </p>
          <h3 class="h1-responsive display-5 font-weight-bold wow fadeInLeft py-3" data-wow-delay="0.3s">Is There Any Side Effect of Blood Donation?</h3>
          <p>The act of blood donation is harmless for healthy adults. It is mandatory to use new/sterile equipment for each blood donor. Therefore, the blood-giving procedure imposes zero risk on donor of getting infected by any disease.</p>
          <p>However, you may feel nausea or dizziness after donating blood. Usually, these symptoms last only for a few minutes. If it happens, you can lie down raising your feet upward until you feel well again.</p>
          <p>You may also experience pain, bleeding, tingling, or numbness at the needle-pierced spot. To overcome these issues quickly, apply a bit pressure on that spot and raise your arm for a few minutes. A bruise may develop at the spot, which will recover soon.

          </p>
